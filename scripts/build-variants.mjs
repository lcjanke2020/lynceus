#!/usr/bin/env node
// Build all sample-app variants under evals/sample-app-variants/.
//
// Each variant directory commits only the files that DIFFER from the
// canonical examples/sample-app/ (typically index.html + src/*). This
// script materializes the shared scaffold — package.json, vite.config.ts,
// tsconfig.json — into each variant at build time, then invokes
// `vite build` per variant. Outputs land in
// evals/sample-app-variants/<name>/dist/, which is what the L4
// scenarios point at via Scenario.variantDir.
//
// Why generate the scaffold instead of committing it per variant: keeps
// the per-variant diff small (1-3 files), makes a vite/typescript bump
// a one-line change in this script rather than 6 separate package.jsons,
// and matches the actual semantics — every variant uses the exact same
// build pipeline as the canonical sample-app.
//
// Each variant uses the root lynceus node_modules via npm's nearest-
// ancestor resolution — no per-variant npm install. The root's
// devDependencies (vite + typescript) are inherited.

import { existsSync, mkdirSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";
import { execSync } from "node:child_process";

const REPO_ROOT = fileURLToPath(new URL("..", import.meta.url));
const VARIANTS_DIR = join(REPO_ROOT, "evals", "sample-app-variants");

const SHARED_VITE_CONFIG = `import { defineConfig } from "vite";
// Auto-generated by scripts/build-variants.mjs — do not edit by hand.
// Matches the canonical examples/sample-app/vite.config.ts:
// sourcemap on, minify off (so identifier names survive into the
// runtime scope for debugger-based introspection).
export default defineConfig({
  build: {
    sourcemap: true,
    minify: false,
  },
});
`;

const SHARED_TSCONFIG = `{
  "compilerOptions": {
    "target": "ES2022",
    "lib": ["ES2022", "DOM"],
    "module": "ESNext",
    "moduleResolution": "Bundler",
    "strict": true,
    "noEmit": true,
    "isolatedModules": true,
    "skipLibCheck": true
  },
  "include": ["src"]
}
`;

const SHARED_PACKAGE_JSON = (variantName) =>
  JSON.stringify(
    {
      name: `lynceus-sample-app-${variantName}`,
      version: "0.0.0",
      private: true,
      type: "module",
      scripts: {
        build: "tsc && vite build",
      },
    },
    null,
    2,
  ) + "\n";

function listVariants() {
  if (!existsSync(VARIANTS_DIR)) return [];
  return readdirSync(VARIANTS_DIR)
    .filter((name) => !name.startsWith("_") && !name.startsWith("."))
    .filter((name) => {
      const full = join(VARIANTS_DIR, name);
      try {
        return statSync(full).isDirectory();
      } catch {
        return false;
      }
    });
}

function materializeScaffold(variantDir, variantName) {
  writeFileSync(join(variantDir, "vite.config.ts"), SHARED_VITE_CONFIG);
  writeFileSync(join(variantDir, "tsconfig.json"), SHARED_TSCONFIG);
  writeFileSync(join(variantDir, "package.json"), SHARED_PACKAGE_JSON(variantName));
}

function buildVariant(variantDir) {
  // Use the root's vite via the local node_modules/.bin. No per-variant
  // install — the variant's package.json declares scripts but no deps,
  // and Node's resolution walks up to repo root for vite + typescript.
  // The canonical sample-app uses its own node_modules but doesn't have
  // to; variants pioneer the lighter-weight pattern.
  execSync("npx tsc && npx vite build", {
    cwd: variantDir,
    stdio: "inherit",
    env: { ...process.env },
  });
}

const variants = listVariants();
if (variants.length === 0) {
  console.log(
    `build-variants: no variants found under ${VARIANTS_DIR} — nothing to build. (compute-step + adversarial-out-of-order use the canonical examples/sample-app/.)`,
  );
  process.exit(0);
}

console.log(`build-variants: ${variants.length} variant(s) — ${variants.join(", ")}`);

for (const name of variants) {
  const dir = join(VARIANTS_DIR, name);
  if (!existsSync(join(dir, "index.html"))) {
    throw new Error(
      `Variant '${name}' has no index.html. Each variant must commit its own index.html under evals/sample-app-variants/<name>/.`,
    );
  }
  console.log(`\n=== building ${name} ===`);
  materializeScaffold(dir, name);
  buildVariant(dir);
}
console.log("\nbuild-variants: all variants built.");
